/* ── Reading overlay (poem/article) ─────── */
let readingSavedY = 0;

function openReading(type, id) {
  readingSavedY = window.scrollY;
  const overlay = $('readingOverlay');
  const body    = $('readingBody');
  const barTitle= $('readingBarTitle');
  if (!overlay || !body) return;

  let html = '';
  if (type === 'poem') {
    const pm = WRITINGS.poems.find(p=>p.id===id);
    if (!pm) return;
    barTitle.textContent = tObj(pm.title);
    html = `
      <div class="reading-title">${tObj(pm.title)}</div>
      <div class="reading-meta">${pm.year} · ${t('poem')}</div>
      <div class="reading-poem-body">${tObj(pm.body)}</div>
    `;
  } else if (type === 'article') {
    const ar = WRITINGS.articles.find(a=>a.id===id);
    if (!ar) return;
    barTitle.textContent = tObj(ar.title);
    const bodyHtml = tObj(ar.body).split(/\n\n+/).map(p=>`<p>${p}</p>`).join('');
    html = `
      <div class="reading-title">${tObj(ar.title)}</div>
      <div class="reading-meta">${ar.year} · ${tObj(ar.type)}</div>
      <div class="reading-article-body">${bodyHtml}</div>
    `;
  }

  body.innerHTML = html;
  document.body.style.overflow = 'hidden';
  overlay.scrollTop = 0;
  overlay.classList.add('open');
}

function closeReading() {
  const overlay = $('readingOverlay');
  if (overlay) overlay.classList.remove('open');
  document.body.style.overflow = '';
  window.scrollTo(0, readingSavedY);
}

/* ── Book reader ─────────────────────────── */
let bookData = null;
let bookSpread = 0; // index of left page (0,2,4,...)

function openBook(id) {
  const bk = WRITINGS.books.find(b=>b.id===id);
  if (!bk) return;
  bookData = bk;
  bookSpread = 0;

  const overlay = $('bookOverlay');
  $('bookBarTitle').textContent = tObj(bk.title);
  overlay.classList.add('open');
  document.body.style.overflow = 'hidden';
  renderBookSpread(false);
}

function closeBook() {
  const overlay = $('bookOverlay');
  if (overlay) overlay.classList.remove('open');
  document.body.style.overflow = '';
  bookData = null;
}

function renderBookSpread(animate) {
  if (!bookData) return;
  const pages = bookData.pages;
  const totalPages = pages.length;

  const L = $('bookPageL');
  const R = $('bookPageR');
  const prevBtn = $('bookPrev');
  const nextBtn = $('bookNext');
  const numEl   = $('bookPageNum');

  const leftIdx  = bookSpread;
  const rightIdx = bookSpread + 1;

  const leftText  = leftIdx  < totalPages ? tObj(pages[leftIdx])  : '';
  const rightText = rightIdx < totalPages ? tObj(pages[rightIdx]) : '';

  if (animate) {
    L.classList.add('flip-out'); R.classList.add('flip-out');
    setTimeout(() => {
      L.classList.remove('flip-out'); R.classList.remove('flip-out');
      setBookPageContent(L, leftText,  leftIdx+1,  totalPages);
      setBookPageContent(R, rightText, rightIdx+1, totalPages);
      L.classList.add('flip-in'); R.classList.add('flip-in');
      setTimeout(() => { L.classList.remove('flip-in'); R.classList.remove('flip-in'); }, 220);
    }, 200);
  } else {
    setBookPageContent(L, leftText,  leftIdx+1,  totalPages);
    setBookPageContent(R, rightText, rightIdx+1, totalPages);
  }

  prevBtn.disabled = bookSpread <= 0;
  nextBtn.disabled = bookSpread + 2 >= totalPages;

  const endPage = Math.min(rightIdx+1, totalPages);
  numEl.textContent = `${leftIdx+1}–${endPage} / ${totalPages}`;
}

function setBookPageContent(el, text, num, total) {
  el.innerHTML = text
    ? `<div class="book-page-text">${text}</div><div class="book-page-num-inside">${num}</div>`
    : `<div class="book-page-num-inside">&nbsp;</div>`;
}

function bookFlip(dir) {
  if (!bookData) return;
  const total = bookData.pages.length;
  const next = bookSpread + dir * 2;
  if (next < 0 || next >= total) return;
  bookSpread = next;
  renderBookSpread(true);
}

