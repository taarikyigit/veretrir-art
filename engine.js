function t(key) { return T[key] ? (T[key][currentLang] || T[key].en) : key; }
function tObj(obj) { return obj ? (obj[currentLang] || obj.en || '') : ''; }
const $ = id => document.getElementById(id);

function setLang(l) {
  currentLang = l;
  localStorage.setItem('lang', l);
  $('b-en').classList.toggle('on', l==='en');
  $('b-tr').classList.toggle('on', l==='tr');
  document.documentElement.lang = l;
  // re-render data-en/data-tr attributes
  document.querySelectorAll('[data-en]').forEach(el => {
    const v = el.getAttribute('data-' + l) || el.getAttribute('data-en');
    if (v !== null) el.innerHTML = v;
  });
  // trigger page-specific refresh
  if (typeof onLangChange === 'function') onLangChange(l);
}

/* ── Init (runs on every page) ─────────── */
function initSite() {
  // set language buttons
  const ben = $('b-en'), btr = $('b-tr');
  if (ben) ben.classList.toggle('on', currentLang==='en');
  if (btr) btr.classList.toggle('on', currentLang==='tr');
  document.documentElement.lang = currentLang;

  // ig link
  const ig = $('ig-link');
  if (ig) ig.href = SITE.instagram;

  // footer
  const fn = $('footer-name'), fd = $('footer-domain');
  if (fn) fn.textContent = SITE.name;
  if (fd) fd.textContent = SITE.domain;

  // data-en/data-tr static labels
  document.querySelectorAll('[data-en]').forEach(el => {
    const v = el.getAttribute('data-' + currentLang) || el.getAttribute('data-en');
    if (v) el.innerHTML = v;
  });

  // page transitions
  document.addEventListener('click', onLinkClick);
}

/* ── Page transitions ───────────────────── */
function onLinkClick(e) {
  const a = e.target.closest('a[href]');
  if (!a) return;
  const href = a.getAttribute('href');
  if (!href || href.startsWith('#') || href.startsWith('mailto:') ||
      href.startsWith('http') || a.target === '_blank') return;
  e.preventDefault();
  navigateTo(href);
}

function navigateTo(href) {
  document.body.classList.add('is-leaving');
  setTimeout(() => { window.location.href = href; }, 370);
}

/* ── Animate page in ────────────────────── */
function animateIn() {
  document.body.classList.add('is-entering');
  setTimeout(() => document.body.classList.remove('is-entering'), 600);
}

