/* Active filters */
let activeFilters = { type:null, year:null, series:null };

function buildFilters() {
  const types   = [...new Set(ARTWORKS.map(a=>a.type))].sort();
  const years   = [...new Set(ARTWORKS.map(a=>a.year))].sort((a,b)=>b-a);
  const series  = [...new Set(ARTWORKS.map(a=>a.series).filter(Boolean))].sort();

  const ftEl = $('filterType');
  const fyEl = $('filterYear');
  const fsEl = $('filterSeries');
  if (!ftEl) return;

  ftEl.innerHTML = chipHTML('All',   null,  'type')  +
    types.map(v=>chipHTML(capitalize(v), v, 'type')).join('');
  fyEl.innerHTML = chipHTML('All',   null,  'year')  +
    years.map(v=>chipHTML(v,           v,  'year')).join('');
  fsEl.innerHTML = series.length
    ? chipHTML('All',  null,  'series') + series.map(v=>chipHTML(capitalize(v), v, 'series')).join('')
    : '';

  updateCount();
}

function chipHTML(label, value, key) {
  const active = activeFilters[key] === value ? 'on' : '';
  return `<button class="filter-chip ${active}" onclick="setFilter('${key}',${JSON.stringify(value)})">${label}</button>`;
}

function setFilter(key, value) {
  activeFilters[key] = activeFilters[key] === value ? null : value;
  buildFilters(); // rebuild chips with updated state
  renderArchive();
}

function resetFilters() {
  activeFilters = {type:null, year:null, series:null};
  buildFilters();
  renderArchive();
}

function filteredWorks() {
  return ARTWORKS.filter(aw => {
    if (activeFilters.type   && aw.type   !== activeFilters.type)   return false;
    if (activeFilters.year   && aw.year   !== activeFilters.year)   return false;
    if (activeFilters.series && aw.series !== activeFilters.series) return false;
    return true;
  });
}

function updateCount() {
  const cnt = $('archiveCount');
  if (cnt) cnt.textContent = filteredWorks().length;
}

function renderArchive() {
  const grid = $('archiveGrid');
  const empty = $('archiveEmpty');
  if (!grid) return;

  const works = filteredWorks();
  updateCount();

  if (works.length === 0) {
    grid.innerHTML = '';
    if (empty) empty.style.display = 'block';
    return;
  }
  if (empty) empty.style.display = 'none';

  grid.innerHTML = works.map(aw => artCardHTML(aw)).join('');
  grid.querySelectorAll('.aw-card').forEach((card, i) => {
    card.onclick = () => openDetail(works[i]);
  });
}

function artCardHTML(aw) {
  const title = tObj(aw.title);
  return `
  <div class="aw-card">
    ${aw.mainImage
      ? `<img src="${aw.mainImage}" alt="${title}">`
      : `<div class="aw-card-ph"><div class="aw-card-ph-cross"></div><span class="aw-card-ph-lbl">image</span></div>`
    }
    <div class="aw-card-overlay">
      <div class="aw-card-title">${title}</div>
      <div class="aw-card-meta">${aw.year} · ${capitalize(aw.type)}</div>
    </div>
  </div>`;
}

/* ── Artwork Detail ─────────────────────── */
let detailSavedY = 0;

function openDetail(aw) {
  detailSavedY = window.scrollY;
  const panel = $('detailPanel');
  const body  = $('detailContent');
  const barTitle = $('detailBarTitle');
  if (!panel || !body) return;

  const title  = tObj(aw.title);
  const medium = tObj(aw.medium);
  const desc   = tObj(aw.description);

  barTitle.textContent = title;

  const heroHtml = aw.mainImage
    ? `<img class="detail-hero" src="${aw.mainImage}" alt="${title}">`
    : `<div class="detail-hero-ph"><span>${t('add_img')}</span></div>`;

  const gallerySlots = Math.max((aw.images||[]).length, 3);
  const galleryHtml = Array.from({length:gallerySlots}, (_,i) => {
    return aw.images && aw.images[i]
      ? `<div class="detail-gal-img"><img src="${aw.images[i]}" alt="${title} ${i+1}"></div>`
      : `<div class="detail-gal-ph"><span>${t('add_img')}</span></div>`;
  }).join('');

  const descHtml = desc.split(/\n\n+/).map(p=>`<p>${p}</p>`).join('');

  const meta = [aw.year, medium, aw.dimensions, aw.series ? capitalize(aw.series) : '']
    .filter(Boolean).join(' · ');

  const model3dBtn = aw.model3d
    ? `<button class="model3d-btn" onclick="openViewer('${aw.model3d}')">${t('view3d')}</button>`
    : '';

  body.innerHTML = `
    ${heroHtml}
    <div class="detail-title">${title}</div>
    <div class="detail-meta">${meta}</div>
    ${model3dBtn}
    <div class="detail-sec">${t('about_work')}</div>
    <div class="detail-desc">${descHtml}</div>
    <div class="detail-sec">${t('add_photos')}</div>
    <div class="detail-gallery">${galleryHtml}</div>
  `;

  document.body.style.overflow = 'hidden';
  panel.scrollTop = 0;
  panel.classList.add('open');

  // update URL without navigating
  history.pushState({aw: aw.id}, '', `artworks.html?id=${aw.id}`);
}

function closeDetail() {
  const panel = $('detailPanel');
  if (!panel) return;
  panel.classList.remove('open');
  document.body.style.overflow = '';
  window.scrollTo(0, detailSavedY);
  history.pushState({}, '', 'artworks.html');
}

