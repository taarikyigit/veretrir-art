function capitalize(s) {
  if (!s) return '';
  return s.charAt(0).toUpperCase() + s.slice(1);
}

/* ── About page re-render on lang change ── */
function onLangChange(l) {
  // artworks archive
  const grid = $('archiveGrid');
  if (grid && ARTWORKS) renderArchive();

  // projects on home
  renderProjects();

  // writings
  renderBooks(); renderPoems(); renderArticles();

  // about text
  const aboutText = $('aboutText');
  if (aboutText && SITE) {
    aboutText.innerHTML = (l==='tr' ? SITE.about.tr : SITE.about.en)
      .split(/\n\n+/).map(p=>`<p>${p.replace(/\n/g,'<br>')}</p>`).join('');
  }

  // contact
  const cnote = $('contactNote');
  if (cnote) cnote.textContent = l==='tr' ? SITE.contact.note.tr : SITE.contact.note.en;
}
