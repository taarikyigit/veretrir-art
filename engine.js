/* Hero slideshow */
let hCur=0, hTotal=0, hTimer;

function renderHero() {
  const nameEl = $('hero-name');
  if (nameEl) nameEl.textContent = SITE.name;

  const track = $('heroTrack');
  if (!track) return;

  const imgs = (HERO_IMAGES && HERO_IMAGES.length) ? HERO_IMAGES : ['','',''];
  hTotal = imgs.length;
  track.innerHTML = '';

  imgs.forEach(src => {
    const s = document.createElement('div');
    s.className = 'hero-slide';
    s.innerHTML = src
      ? `<img src="${src}" alt="">`
      : `<div class="hero-slide-ph"><span>add artwork photo</span></div>`;
    track.appendChild(s);
  });

  heroInitDots();
  clearInterval(hTimer);
  hTimer = setInterval(() => heroMove(1), 5000);
}

function heroInitDots() {
  const el = $('heroDots');
  if (!el) return;
  el.innerHTML = '';
  for (let i=0;i<hTotal;i++) {
    const d = document.createElement('button');
    d.className = 'hero-dot'+(i===0?' on':'');
    d.onclick = () => heroGoTo(i);
    el.appendChild(d);
  }
  heroGoTo(0);
}

function heroGoTo(n) {
  hCur = (n+hTotal)%hTotal;
  const track = $('heroTrack');
  if (track) track.style.transform = `translateX(-${hCur*100}%)`;
  const dots = $('heroDots');
  if (dots) dots.querySelectorAll('.hero-dot').forEach((d,i) => d.classList.toggle('on',i===hCur));
}

function heroMove(dir) {
  clearInterval(hTimer);
  heroGoTo(hCur+dir);
  hTimer = setInterval(() => heroMove(1), 5000);
}

/* Swipe on hero */
window.addEventListener('DOMContentLoaded', () => {
  const track = $('heroTrack');
  if (!track) return;
  let dx=0;
  track.addEventListener('mousedown',  e=>dx=e.clientX);
  track.addEventListener('touchstart', e=>dx=e.touches[0].clientX,{passive:true});
  track.addEventListener('mouseup',    e=>{if(Math.abs(e.clientX-dx)>40)heroMove(e.clientX<dx?1:-1);});
  track.addEventListener('touchend',   e=>{const d=e.changedTouches[0].clientX-dx;if(Math.abs(d)>40)heroMove(d<0?1:-1);});
});

